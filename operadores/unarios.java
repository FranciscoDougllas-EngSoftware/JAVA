public class unarios {
    public static void main(String[] args) {
        int c = 12;
        System.out.println(--c);  // Pre-decrement: decrements then prints
        System.out.println(c++);  // Post-increment: prints then increments
        System.out.println(c--);
        System.out.println(c);    // Shows current value
        System.out.println(-c);   // Unary minus operator
        System.out.println(c);    // Shows current value
    }
}
